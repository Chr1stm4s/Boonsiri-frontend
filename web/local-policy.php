					<h5 class="fs-1">ข้อ 6: คุกกี้ (Cookie) พิกเซล (Pixel) และ ไอพีแอดเดรส (IP Address)</h5>
					<p>นอกจากข้อมูลส่วนบุคคล บริษัทอาจเก็บรวบรวมข้อมูลบางอย่างผ่านเทคโนโลยี เช่น คุกกี้ (Cookie) พิกเซล (Pixel) ไอพี แอดเดรส (IP Address) และแหล่งเก็บข้อมูลบนเบราว์เซอร์และอุปกรณ์ของผู้ใช้ รวมถึงเครื่องมือเก็บข้อมูลของบุคคลภายนอก เช่น ผู้ให้บริการด้านการตลาดหรือวิเคราะห์ ในการเก็บรวบรวมข้อมูลโดยใช้เทคโนโลยีประเภทนี้ โดยตรงบนเว็บไซต์</p>
					<p>ทั้งนี้ คุกกี้ (Cookie) คือ ไฟล์ข้อมูลตัวอักษรขนาดเล็กที่รวบรวมข้อมูลในคอมพิวเตอร์ โทรศัพท์เคลื่อนที่ หรือ อุปกรณ์อื่นของผู้ใช้งาน คุกกี้ทําให้บริษัททราบถึงบริการหรือสินค้าที่ท่านชื่นชอบ รวมถึงบริการที่ไม่อยู่ในความสนใจของท่าน ส่วนพิกเซล (Pixel) เป็นภาพขนาดเล็ก เป็นส่วนหนึ่งของรหัสบนเว็บเพจ ซึ่งทําหน้าที่ต่างๆ เช่น ทําให้เซิร์ฟเวอร์ อื่นสามารถวัดยอดการเข้าชมเว็บไซต์ แอปพลิเคชัน และมักใช้เชื่อมโยงกับคุกกี้ โดยรหัสดังกล่าวจะทําการติดตามเมื่อมี การดาวน์โหลดพิกเซลเพื่อบ่งชี้ว่าผู้ใช้งานได้เข้าเชื่อมต่อกับจอภาพใด หรือบางส่วนของจอภาพใด</p>
					<p>ด้วยการทํางานของคุกกี้และพิกเซล จะทําให้บริษัทสามารถจดจําผู้ใช้บริการที่เคยเข้าชมเว็บไซต์ และ แอปพลิเคชันของบริษัท ทราบถึงจํานวนและวัตถุประสงค์ของผู้ที่ใช้บริการเว็บไซต์ แอปพลิเคชัน และช่วยให้บริษัทจัด บริการข้อมูล พัฒนาประสบการณ์การเข้าชมเว็บไซต์และแอปพลิเคชัน ปรับปรุงคุณภาพการให้บริการ นําเสนอบริการ สิทธิพิเศษ ผลิตภัณฑ์ กิจกรรม หรือโฆษณาที่ตรงกับพฤติกรรม และความสนใจสําหรับท่านด้วย</p>
