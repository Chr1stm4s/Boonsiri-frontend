					<p>ทั้งนี้ การเปิดเผยข้อมูลส่วนบุคคลของท่านให้กับบุคคลอื่น จะดำเนินการภายใต้วัตถุประสงค์ที่กำหนดข้างต้นหรือวัตถุประสงค์อื่นที่กฎหมายกำหนดให้กระทำได้เท่านั้น ในกรณีที่กฎหมายกำหนดว่าต้องได้รับความยินยอมจากท่าน บริษัทจะดำเนินการขอความยินยอมโดยชัดแจ้งจากท่านก่อน โดยบริษัทจะจัดให้มีมาตรการที่เหมาะสมเพื่อคุ้มครองข้อมูลส่วนบุคคลที่ได้เปิดเผย และเพื่อปฏิบัติตามกฎหมายว่าด้วยการคุ้มครองข้อมูลส่วนบุคคล</p>
					<p>นโยบายส่วนบุคคลนี้ไม่ครอบคลุมการรวบรวมและการใช้ข้อมูลส่วนบุคคลของบุคคลภายนอก ที่กระทำโดยบุคคลภายนอก ไม่ว่าจะเป็นผู้โฆษณา ผู้ให้บริการ ผู้ขายสินค้าในเว็บไซต์อื่นๆ ที่เชื่อมต่อ (Link) กับเว็บไซต์, แอปพลิเคชัน, โซเชียลมีเดีย (Social Media) หรือช่องทางในอนาคตของบริษัท การเชื่อมโยงระบบโซเชียลมีเดีย (Social Media) เพื่อแชร์เนื้อหาในช่องทางของบริษัท หากการกระทำใดๆ ก็ตามที่ขัดต่อกฎหมาย และก่อให้เกิดความเสียหายแก่บริษัท ทางบริษัทจะไม่รับผิดชอบในความเสียหายใดๆ อันเนื่องมาจากการกระทำหรือการรวบรวมข้อมูลโดยบุคคลภายนอก</p>

