					<p>คุกกี้ประเภทนี้ จะถูกลบออกหลังจากการเข้าชมเว็บไซต์ของท่าน หากท่านปิดการทํางานของคุกกี้เหล่านี้ ท่าน อาจจะไม่สามารถใช้บริการเว็บไซต์ของบริษัทได้ตามต้องการ เช่น การอ่าน และการนําทางระหว่างหน้าเว็บ</p>

					<h5>คุกกี้ด้านประสิทธิภาพการทํางานของเว็บไซต์ (Performance cookies)</h5>
					<p> บริษัทใช้คุกกี้ประเภทนี้เพื่อรวบรวมข้อมูลทางสถิติเกี่ยวกับวิธีการเข้าและพฤติกรรมการเยี่ยมชมเว็บไซต์ เพื่อวิเคราะห์และนับจํานวนผู้ใช้งาน ตลอดจนช่วยให้บริษัททราบถึงพฤติกรรมการเยี่ยมชมเว็บไซต์ของท่าน ซึ่งจะช่วยปรับปรุงการทํางานของเว็บไซต์ โดยให้ท่านสามารถค้นหาสิ่งที่ต้องการได้อย่างง่ายดาย</p>
					<p>นอกจากนี้ บริษัทอาจใช้คุกกี้เหล่านี้เพื่อเน้นย้ําบทความ หรือบริการของเว็บไซต์ ที่บริษัทคิดว่าท่านน่าจะสนใจ และวัดความมีประสิทธิผลของเนื้อหาและโฆษณาของบริษัทโดยพิจารณาจากการใช้งานเว็บไซต์ของท่าน</p>
					<p>อย่างไรก็ตาม ข้อมูลที่ได้และใช้ประมวลผลนั้น ไม่มีการระบุชื่อ หรือบ่งบอกความเป็นตัวตนของท่านได้ อีกทั้ง ไม่มีการเก็บข้อมูลจําเพาะบุคคล เช่น ชื่อ อีเมล เป็นต้น และใช้เป็นข้อมูลทางสถิติเท่านั้น</p>

					<h5>คุกกี้เพื่อปรับเนื้อหาเข้ากับกลุ่มเป้าหมาย (Targeting Cookies)</h5>
					<p>บริษัทใช้คุกกี้ประเภทนี้ในการบันทึกและจดจําคุณลักษณะต่างๆ ที่ท่านได้เลือกในขณะเข้าชมเว็บไซต์ ของ บริษัทบุญศิริโฟรเซ่นโปรดักส์ จำกัด เช่น หน้าที่ท่านได้เยี่ยมชม หมวดหมู่ และเนื้อหาที่ท่านชอบอ่าน บริษัทจะบันทึกข้อมูลเหล่านี้ และนํากลับ มาใช้อีกครั้งเมื่อท่านกลับเข้ามาที่เว็บไซต์ของบริษัทอีกครั้ง เพื่อปรับให้ท่านได้รับชมเนื้อหา รวมถึงนําเสนอเนื้อหาที่ เกี่ยวข้อง ได้ตรงกับความชอบของท่านให้มากที่สุด</p>
					<p>ข้อมูลที่ได้และใช้ประมวลผลนั้นไม่มีการระบุชื่อ หรือบ่งบอกความเป็นตัวตนของท่านได้ อีกทั้งไม่มีการเก็บ ข้อมูลจําเพาะบุคคล เช่น ชื่อ อีเมล เป็นต้น และใช้เป็นข้อมูลทางสถิติเท่านั้น</p>
					<p>หากท่านปิดการใช้งานของคุกกี้ประเภทนี้ อาจทําให้ท่านไม่สามารถใช้งานฟังก์ชันบางอย่างได้ หรือมี ประสบการณ์การใช้งานที่ไม่ราบรื่น</p>

					<h5>คุกกี้เพื่อการโฆษณา (Advertising Cookies)</h5>
					<p>บริษัทใช้คุกกี้ประเภทนี้เพื่อจดจําพฤติกรรมการอ่านเนื้อหาบนเว็บไซต์ ของ บริษัทบุญศิริโฟรเซ่นโปรดักส์ จำกัด ของท่าน รวมถึงราย ละเอียดของอุปกรณ์ที่ท่านใช้ เพื่อนําไปใช้วิเคราะห์ในการนําเสนอโฆษณาที่เหมาะสมกับท่านมากที่สุด รวมถึงป้องกัน หรือจํากัดจํานวนครั้งที่ท่านจะเห็นโฆษณานั้นซ้ําๆ และช่วยวัดความมีประสิทธิผลของโฆษณาที่บริษัทนําเสนอด้วย</p>
					<p>นอกจากนี้ บริษัทยังมีการใช้บริการคุกกี้ที่จัดหาให้โดยบุคคลภายนอกเว็บไซต์นี้ โดยระบบของบุคคล ภายนอกจะวางคุกกี้โฆษณาไว้สองช่วงเวลา นั่นคือเมื่อท่านเข้าชมเว็บไซต์ของบริษัท และเมื่อท่านใช้บริการของพวกเขา และออกจากเว็บไซต์ของบริษัท </p>

					<h4>การเชื่อมโยงอุปกรณ์ของบุคคลภายนอกผ่านบทความในเว็บไซต์ และช่องทางอื่นๆ ในเครือของบริษัท</h4>
					<p>เพื่อช่วยเสริมสร้างประสบการณ์การเข้าเว็บไซต์ แอปพลิเคชัน หรือช่องทางอื่นของบริษัท บุญศิริโฟรเซ่นโปรดักส์ จำกัด ในบางครั้ง บริษัทอาจ เชื่อมโยงข้อมูลกับโซเชียลมีเดีย เข้ากับเครื่องมือของบุคคลภายนอก เช่น Youtube, Twitter, Facebook, Pinterest, Instagram เมื่อท่านเข้าดูบทความภายในเว็บไซต์ แอปพลิเคชัน หรือช่องทางอื่นๆ ของบริษัท</p>

					<hr class="my-5">

					<h5 class="fs-1">ข้อ 7: การคุ้มครองข้อมูลการเงิน</h5>
					<p>การรักษาความลับข้อมูลด้านการเงินส่วนบุคคล เป็นเรื่องที่สําคัญอย่างยิ่งต่อบริษัท บริษัทจะไม่เผยแพร่ข้อมูลดังกล่าวต่อบุคคลภายนอก เว้นแต่เท่าที่ระบุไว้ในนโยบายส่วนบุคคลฉบับนี้ ดังนั้น ข้อมูลสถิติของผู้ใช้บริการ บริษัทอาจเปิดเผยข้อมูลที่ท่านให้ความยินยอมต่อผู้โฆษณา หรือพันธมิตรทางธุรกิจเกี่ยวกับบริการทางการเงิน รวมถึงการใช้บริการของลูกค้าของบริษัท</p>

					<hr class="my-5">

					<h5 class="fs-1">ข้อ 8: ระยะเวลาในการเก็บข้อมูลส่วนบุคคลของท่าน</h5>
					<p>บริษัทตระหนักถึงความสำคัญของการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลของท่าน บริษัทจึงกำหนดให้มีมาตรการในการรักษาความมั่นคงปลอดภัยของข้อมูลส่วนบุคคลอย่างเหมาะสม และสอดคล้องกับการรักษาความลับของข้อมูลส่วนบุคคลเพื่อป้องกันการสูญหาย การเข้าถึง ทำลาย ใช้ แปลง แก้ไขหรือเปิดเผยข้อมูลส่วนบุคคลโดยไม่มีสิทธิหรือโดยไม่ชอบด้วยกฎหมาย ตลอดจนการป้องกันมิให้มีการนำข้อมูลส่วนบุคคลไปใช้โดยมิได้รับอนุญาต และดำเนินมาตรการที่เหมาะสมเพื่อคุ้มครองสิทธิของท่าน เพื่อให้เป็นไปตามกฎหมายคุ้มครองข้อมูลส่วนบุคคล โดยข้อมูลส่วนบุคคลของท่านที่บริษัทได้รับมาจะถูกนำไปใช้ให้เป็นไปตามวัตถุประสงค์การดำเนินงานของบริษัทเท่านั้น </p>
					
					<hr class="my-5">

					<h5 class="fs-1">ข้อ 9 : กล้องวงจรปิด (CCTV)</h5>
					<p> บริษัท บุญศิริโฟรเซ่นโปรดักส์ จำกัด กำลังดำเนินการใช้กล้องวงจรปิด (CCTV) สำหรับการเฝ้าระวังสังเกตการณ์ในพื้นที่ภายในและรอบบริเวณโดยรอบ (“พื้นที่”) ของเรา เพื่อการปกป้องชีวิต สุขภาพ และทรัพย์สิน ทั้งนี้ เราทำการเก็บรวบรวมข้อมูลส่วนบุคคลของเจ้าหน้าที่ ผู้ปฏิบัติงาน ลูกค้า ลูกจ้าง ผู้รับเหมา ผู้มาติดต่อ หรือ บุคคลใด ๆ (ซึ่งต่อไปนี้จะเรียกรวมกันว่า “ท่าน”) ที่เข้ามายังพื้นที่ โดยผ่านการใช้งานอุปกรณ์กล้องวงจรปิดดังกล่าว ประกาศความเป็นส่วนตัวในการใช้กล้องวงจรปิด (“ประกาศ”) ฉบับนี้ให้ข้อมูลเกี่ยวกับการดำเนินการเก็บรวบรวม ใช้หรือเปิดเผย ซึ่งข้อมูลที่สามารถทำให้สามารถระบุตัวท่านได้ (“ข้อมูลส่วนบุคคล”) รวมทั้งสิทธิต่าง ๆ ของท่าน ดังนี้ </p>

					<h5 class="fs-3">9.1 ฐานกฎหมายในการประมวลผลข้อมูลส่วนบุคคล</h5>
					<p> <strong>เราดำเนินการเก็บรวบรวมข้อมูลส่วนบุคคลของท่านภายใต้ฐานกฎหมายดังต่อไปนี้</strong></p>
					<ul class="list-unstyled">
						<li>❒ ความจำเป็นในการป้องกันหรือระงับอันตรายต่อชีวิต ร่างกาย หรือสุขภาพของท่านหรือบุคคลอื่น</li>
						<li>❒ ความจำเป็นเพื่อประโยชน์โดยชอบด้วยกฎหมายของเราหรือบุคคลอื่น โดยประโยชน์ดังกล่าวมีความสำคัญไม่น้อยไปกว่าสิทธิขั้นพื้นฐานในข้อมูลส่วนบุคคลของท่าน</li>
						<li>❒ ความจำเป็นในการปฏิบัติตามกฎหมายที่เกี่ยวข้อง ซึ่งควบคุมดูแลเกี่ยวกับความปลอดภัยและสภาพแวดล้อมในสถานที่ทำงาน และทรัพย์สินของเรา</li>
					</ul>

					<h5 class="fs-3">9.2 วัตถุประสงค์ในการเก็บรวบรวมข้อมูลส่วนบุคคลของท่าน</h5>
					<p><strong>เราดำเนินการเก็บรวบรวมข้อมูลส่วนบุคคลของท่านเพื่อวัตถุประสงค์ ดังต่อไปนี้</strong> </p>
					<ol>
						<li>เพื่อการปกป้องสุขภาพและความปลอดภัยส่วนตัวของท่าน ซึ่งรวมไปถึงทรัพย์สินของท่าน</li>
						<li>เพื่อการปกป้องอาคาร สิ่งอำนวยความสะดวกและทรัพย์สินของเราจากความเสียหาย การขัดขวาง การทำลายซึ่งทรัพย์สินหรืออาชญากรรมอื่น</li>
						<li>เพื่อสนับสนุนหน่วยงานที่เกี่ยวข้องในการบังคับใช้กฎหมายเพื่อการยับยั้ง ป้องกัน สืบค้น และ ดำเนินคดีทางกฎหมาย</li>
						<li>เพื่อการให้ความช่วยเหลือในกระบวนการระงับข้อพิพาทซึ่งเกิดขึ้นในระหว่างที่มีกระบวนการทางวินัยหรือกระบวนการร้องทุกข์</li>
						<li>เพื่อการให้ความช่วยเหลือในกระบวนการสอบสวน หรือ กระบวนการเกี่ยวกับการส่งเรื่องร้องเรียน</li>
						<li>เพื่อการให้ความช่วยเหลือในกระบวนการริเริ่มหรือป้องกันการฟ้องร้องทางแพ่ง ซึ่งรวมไปถึงแต่ไม่จำกัดเพียงการดำเนินการทางกฎหมายที่เกี่ยวข้องกับการจ้างงาน</li>
						<li>อื่นๆ (หากมี)</li>
					</ol>

					<h5 class="fs-3">9.3 ข้อมูลส่วนบุคคลที่เราเก็บรวบรวมและใช้</h5>
					<p>ตามวัตถุประสงค์ตามที่ได้แจ้งในข้อ 2. เราทำการติดตั้งกล้องวงจรปิดในตำแหน่งที่มองเห็นได้ โดยจะจัดวางป้ายเตือนว่ามีการใช้งานกล้องวงจรปิด ณ ทางเข้าและทางออก รวมถึงพื้นที่ที่เราเห็นสมควรว่าเป็นจุดที่ต้องมีการเฝ้าระวัง เพื่อเก็บรวบรวมข้อมูลส่วนบุคคลของท่านเมื่อท่านเข้ามายังพื้นที่ ดังต่อไปนี้ เพื่อให้ท่านได้รับสินค้าและบริการที่ดีจากบริษัทอย่างมีประสิทธิภาพ โดยท่านตกลงและยินยอมให้บริษัทสามารถใช้ เปิดเผย ข้อมูลส่วนบุคคลของท่านในการติดต่อหรือส่งข้อมูลส่วนบุคคลของท่านทั้งหมดหรือบางส่วนไปยังบริษัทในเครือของบริษัทหรือพันธมิตรในทางธุรกิจ หรือบุคคลอื่น โดยไม่จำเป็นต้องบอกกล่าวล่วงหน้าแก่ท่าน เพื่อประโยชน์ของท่านและของกิจการบางส่วน ดังต่อไปนี้</p>

					<h5 class="fs-5">รายการข้อมูลส่วนบุคคลที่เก็บรวบรวม</h5>
					<ul class="list-unstyled">
						<li>❒ ภาพนิ่ง</li>
						<li>❒ ภาพเคลื่อนไหว</li>
						<li>❒ เสียง</li>
						<li>❒ ภาพทรัพย์สินของท่าน เช่น พาหนะ กระเป๋า หมวก เครื่องแต่งกาย เป็นต้น</li>
					</ul>
					<p>ทั้งนี้ เราจะไม่ทำการติดตั้งกล้องวงจรปิดในพื้นที่ที่อาจล่วงละเมิดสิทธิขั้นพื้นฐานของท่านจนเกินสมควร ได้แก่ ห้องพัก ห้องน้ำ ห้องอาบน้ำ หรือสถานที่เพื่อใช้ในการพักผ่อนของผู้ปฏิบัติงาน</p>

					<h5 class="fs-3">9.4 การเปิดเผยข้อมูลส่วนบุคคลของท่าน</h5>
					<p>เราจะเก็บรักษาข้อมูลในกล้องวงจรปิดที่เกี่ยวกับท่านไว้เป็นความลับ และจะไม่ทำการเปิดเผย เว้นแต่ กรณีที่เรามีความจำเป็นเพื่อให้สามารถบรรลุวัตถุประสงค์ ในการเฝ้าระวังสังเกตการณ์ตามที่ได้ระบุในประกาศฉบับนี้ เราอาจเปิดเผยข้อมูลในกล้องวงจรปิดแก่ประเภทของบุคคลหรือนิติบุคคล ดังต่อไปนี้</p>
					<ol>
						<li>หน่วยงานที่มีอำนาจหน้าที่ตามที่กฎหมายกำหนด เพื่อช่วยเหลือ สนับสนุนในการบังคับใช้กฎหมาย หรือเพื่อการดำเนินการสืบสวน สอบสวน หรือการดำเนินคดีความต่าง ๆ</li>
						<li>ผู้ให้บริการซึ่งเป็นบุคคลภายนอก เพื่อความจำเป็นในการสร้างความมั่นใจในเรื่องการป้องกันหรือระงับอันตรายต่อชีวิต ร่างกาย สุขภาพ รวมทั้งทรัพย์สินของท่านหรือบุคคลอื่น </li>
					</ol>

					<h5 class="fs-3">9.5 สิทธิตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 ของท่าน </h5>
					<p>พระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 มีวัตถุประสงค์เพื่อให้ข้อมูลส่วนบุคคลของท่านอยู่ในความควบคุมของท่านได้มากขึ้น โดยท่านสามารถใช้สิทธิตามพระราชบัญญัติคุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562 เมื่อบทบัญญัติในส่วนที่เกี่ยวกับสิทธิของเจ้าของข้อมูลส่วนบุคคลมีผลใช้บังคับ ซึ่งมีรายละเอียดดังต่อไปนี้</p>
					<ol>
						<li>สิทธิในการเข้าถึง รับสำเนา และขอให้เปิดเผยที่มาของข้อมูลส่วนบุคคลของท่านที่เราเก็บรวบรวมอยู่ เว้นแต่กรณีที่เรามีสิทธิปฏิเสธคำขอของท่านตามกฎหมายหรือคำสั่งศาล หรือกรณีที่คำขอของท่านจะมีผลกระทบที่อาจก่อให้เกิดความเสียหายต่อสิทธิและเสรีภาพของบุคคลอื่น </li>
						<li>สิทธิในการขอแก้ไขข้อมูลส่วนบุคคลของท่านที่ไม่ถูกต้องหรือไม่ครบถ้วน เพื่อให้มีความถูกต้อง เป็นปัจจุบัน สมบูรณ์ และไม่ก่อให้เกิดความเข้าใจผิด </li>
						<li>
							สิทธิในการขอให้ระงับการใช้ข้อมูลส่วนบุคคลของท่านในกรณีหนึ่งกรณีใด ดังต่อไปนี้
							<ol>
								<li>เมื่ออยู่ในช่วงเวลาที่เราทำการตรวจสอบตามคำร้องขอของท่านให้แก้ไขข้อมูลส่วนบุคคลของท่านให้ถูกต้อง สมบูรณ์และเป็นปัจจุบัน</li>
								<li>ข้อมูลส่วนบุคคลของท่านถูกเก็บรวบรวม ใช้หรือเปิดเผยโดยมิชอบด้วยกฎหมาย</li>
								<li>เมื่อข้อมูลส่วนบุคคลของท่านหมดความจำเป็นในการเก็บรักษาไว้ตามวัตถุประสงค์เราได้แจ้งไว้ในการเก็บรวบรวม แต่ท่านประสงค์ให้เราเก็บรักษาข้อมูลนั้นต่อไปเพื่อประกอบการใช้สิทธิตามกฎหมายของท่าน</li>
								<li>เมื่ออยู่ในช่วงเวลาที่เรากำลังพิสูจน์ให้ท่านเห็นถึงเหตุอันชอบด้วยกฎหมายในการเก็บรวบรวม ใช้หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน หรือตรวจสอบความจำเป็นในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านเพื่อประโยชน์สาธารณะ อันเนื่องมาจากการที่ท่านได้ใช้สิทธิคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน</li>
							</ol>
						</li>
						<li>สิทธิในการคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่าน เว้นแต่กรณีที่เราเหตุในการปฏิเสธคำขอของท่านโดยชอบด้วยกฎหมาย (เช่น เราสามารถแสดงให้เห็นว่าการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านมีเหตุอันชอบด้วยกฎหมายยิ่งกว่า หรือเพื่อการก่อตั้งสิทธิเรียกร้องตามกฎหมาย การปฏิบัติตามหรือการใช้สิทธิเรียกร้องทางกฎหมาย หรือเพื่อประโยชน์สาธารณะตามภารกิจของเรา)</li>
					</ol>

					<h5 class="fs-3">9.6 ระยะเวลาในการเก็บรักษาข้อมูลส่วนบุคคล</h5>
					<p>เพื่อบรรลุวัตถุประสงค์ของการเฝ้าระวังสังเกตโดยการใช้อุปกรณ์กล้องวงจรปิดตามที่ประกาศนี้กำหนด เราจะเก็บรักษาข้อมูลส่วนบุคคลในกล้องวงจรปิดที่เกี่ยวข้องกับท่าน เป็นระยะเวลา 45 วัน ทั้งนี้ เมื่อพ้นระยะเวลาดังกล่าวเราจะทำการ ลบ ทำลายข้อมูลส่วนบุคคลของท่านต่อไป</p>

					<h5 class="fs-3">9.7 การรักษาความมั่นคงปลอดภัยข้อมูลส่วนบุคคล</h5>
					<p>เพื่อบรรลุวัตถุประสงค์ของการเฝ้าระวังสังเกตโดยการใช้อุปกรณ์กล้องวงจรปิดตามที่ประกาศนี้กำหนด เราจะเก็บรักษาข้อมูลส่วนบุคคลในกล้องวงจรปิดที่เกี่ยวข้องกับท่าน เป็นระยะเวลา 45 วัน ทั้งนี้ เมื่อพ้นระยะเวลาดังกล่าวเราจะทำการ ลบ ทำลายข้อมูลส่วนบุคคลของท่านต่อไป</p>
					<p>นอกจากนี้ เราได้กำหนดให้มีนโยบายการคุ้มครองข้อมูลส่วนบุคคลขึ้นโดยประกาศให้ทราบกันโดยทั่วทั้งองค์กร พร้อมแนวทางปฏิบัติเพื่อให้เกิดความมั่นคงปลอดภัยในการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคล โดยธำรงไว้ซึ่งความเป็นความลับ (Confidentiality) ความถูกต้องครบถ้วน (Integrity) และสภาพพร้อมใช้งาน (Availability) ของข้อมูลส่วนบุคคล โดยเราได้จัดให้มีการทบทวนนโยบายดังกล่าวรวมถึงประกาศนี้ในระยะเวลาตามที่เหมาะสม</p>

					<h5 class="fs-3">9.8 ความรับผิดชอบของผู้ควบคุมข้อมูลส่วนบุคคล</h5>
					<p>เราได้กำหนดให้เจ้าหน้าที่เฉพาะผู้ที่มีอำนาจหน้าที่เกี่ยวข้องในการจัดเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของกิจกรรมการประมวลผลนี้เท่านั้นที่จะสามารถเข้าถึงข้อมูลส่วนบุคคลของท่านได้ โดยเราจะดำเนินการให้เจ้าหน้าที่ปฏิบัติตามประกาศนี้อย่างเคร่งครัด</p>

					<hr class="my-5">

					<h5 class="fs-1">ข้อ 10: สิทธิของเจ้าของข้อมูลส่วนบุคคล</h5>
					<p><strong>เจ้าของข้อมูลส่วนบุคคลมีสิทธิในการดำเนินการ ดังนี้</strong></p>
					<ol>
						<li><strong>การเพิกถอนความยินยอม</strong> : ท่านมีสิทธิที่จะเพิกถอนความยินยอมของท่านในการจัดเก็บ ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของท่านตลอดระยะเวลาที่ข้อมูลส่วนบุคคลของท่านอยู่กับบริษัท</li>
						<li><strong>การขอเข้าถึงข้อมูลส่วนบุคคล</strong> : ท่านสามารถขอเข้าถึง ขอรับสำเนาข้อมูลส่วนบุคคลของท่าน หรือขอให้เปิดเผยการได้มาซึ่งข้อมูลส่วนบุคคล ทั้งนี้ บริษัทอาจปฏิเสธคำขอของท่านได้ตามที่กฎหมายกำหนด หรือตามคำสั่งศาล</li>
						<li><strong>การได้รับข้อมูลอิเล็กทรอนิกส์และถ่ายโอนข้อมูล</strong> : ท่านมีสิทธิที่จะได้รับข้อมูลที่ได้ให้ไว้กับบริษัทในรูปแบบอิเล็กทรอนิกส์ และมีสิทธิในการถ่ายโอนข้อมูลของท่านสำหรับการใช้งานในบริการของบุคคลอื่น และในการใช้สิทธิดังกล่าวนี้ ท่านมีสิทธิที่จะให้บริษัทถ่ายโอนข้อมูลไปยังบุคคลอื่นๆ โดยตรง หากสามารถกระทำได้โดยทางเทคนิค</li>
						<li>
							<strong>การคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคล</strong> : ท่านมีสิทธิคัดค้านการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลเกี่ยวกับท่าน ในกรณีดังนี้
							<ol>
								<li>กรณีที่เป็นการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลเพื่อวัตถุประสงค์เกี่ยวกับการตลาดแบบตรง</li>
								<li>กรณีที่เป็นการเก็บรวบรวม ใช้ หรือ เปิดเผยข้อมูลส่วนบุคคลเพื่อวัตถุประสงค์เกี่ยวกับการศึกษาวิจัยทางวิทยาศาสตร์ ประวัติศาสตร์ หรือสถิติ เว้นแต่กรณีจำเป็นเพื่อการดำเนินภารกิจเพื่อประโยชน์สาธารณะของบริษัท</li>
								<li>กรณีที่เป็นข้อมูลที่เก็บรวบรวมไว้ด้วยเหตุจำเป็น เพื่อการดำเนินภารกิจเพื่อประโยชน์สาธารณะของบริษัท หรือเหตุจำเป็นเพื่อประโยชน์โดยชอบด้วยกฎหมายของบริษัท เว้นแต่บริษัทแสดงให้เห็นถึงเหตุอันชอบด้วยกฎหมายที่สำคัญยิ่งกว่า หรือเป็นไปเพื่อก่อตั้งสิทธิเรียกร้องตามกฎหมาย การปฏิบัติตาม หรือการใช้สิทธิเรียกร้องตามกฎหมาย หรือการยกขึ้นต่อสู้สิทธิเรียกร้องตามกฎหมาย</li>
								<li>การขอลบ หรือเปลี่ยนแปลงสถานะข้อมูลส่วนบุคคล : ท่านสามารถขอให้ลบหรือทำลาย หรือทำให้ข้อมูลส่วนบุคคลของท่านกลายเป็นข้อมูลที่ไม่ระบุตัวบุคคล เว้นแต่เป็นกรณีที่บริษัทต้องปฏิบัติตามกฎหมายที่เกี่ยวข้องในการเก็บรักษาข้อมูลดังกล่าว</li>
								<li>การขอให้ระงับการประมวลผลข้อมูลส่วนบุคคล : ท่านสามารถขอให้บริษัทระงับการประมวลผลข้อมูลส่วนบุคคลของท่านทั้งหมด หรือบางส่วน เป็นการชั่วคราวหรือถาวร</li>
								<li>การแก้ไขข้อมูลส่วนบุคคลให้ถูกต้อง : ท่านสามารถขอแก้ไข หรือเปลี่ยนแปลงข้อมูลส่วนบุคคลของท่าน ในกรณีที่ข้อมูลส่วนบุคคลของท่านไม่ถูกต้องตามความเป็นจริง และทำให้ข้อมูลของท่านเป็นปัจจุบัน</li>
								<li>การร้องเรียน : ท่านมีสิทธิร้องเรียนต่อหน่วยงานของรัฐที่เกี่ยวข้อง ในกรณีที่ท่านเชื่อว่าการเก็บรวบรวม ใช้ หรือเปิดเผยข้อมูลส่วนบุคคลของบริษัทนั้นฝ่าฝืนหรือไม่ปฏิบัติตามพ.ร.บ.คุ้มครองข้อมูลส่วนบุคคล พ.ศ. 2562</li>
							</ol>
						</li>
					</ol>

					<hr class="my-5">

